describe('assignCharacterIdentifiers', () => {
  it('creates unique ASCII idents and avoids narrator slot', () => {
    const idents = assignCharacterIdentifiers(['白樱', 'narrator', '白樱']);
    const values = Array.from(idents.values());
    expect(new Set(values).size).toBe(values.length);
    expect(values).not.toContain('v');
  });

  it('renames clashing idents with numeric suffix', () => {
    const idents = assignCharacterIdentifiers(['Hana', 'Hana!']);
    const values = Array.from(idents.values());
    expect(values).toEqual(['hana', 'hana2']);
  });

  it('falls back to a safe base when name has no ASCII', () => {
    const idents = assignCharacterIdentifiers(['白樱', '黒猫']);
    for (const v of idents.values()) {
      expect(v).toMatch(/^[a-z_][a-z0-9_]*$/);
    }
  });
});

describe('assignSceneIdentifiers', () => {
  it('sanitizes scene names into ident-safe slugs', () => {
    const idents = assignSceneIdentifiers(['Sakura Night!', '教室']);
    const values = Array.from(idents.values());
    expect(values[0]).toBe('sakura_night');
    expect(values[1]).toMatch(/^[a-z_][a-z0-9_]*$/);
  });
});

describe('renderScriptRpy', () => {
  const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE);

  it('emits a header, image defs, character defs, transforms and a start label', () => {
    expect(script).toContain('Auto-generated by renpy-agent');
    expect(script).toContain('image bg_sakura_night = Solid(');
    expect(script).toContain('image bg_classroom = Solid(');
    expect(script).toMatch(/define \w+ = Character\("白樱"/);
    expect(script).toContain('define v = Character(None');
    expect(script).toContain('transform stand:');
    expect(script).toContain('label start:');
  });

  it('switches backgrounds when scene changes and holds when it does not', () => {
    const sceneLines = script.split('\n').filter((l) => l.includes('scene bg_'));
    expect(sceneLines[0]).toContain('scene bg_sakura_night');
    expect(sceneLines[1]).toContain('scene bg_classroom');
    // Only two scene switches + one final bg_black
    expect(sceneLines).toHaveLength(3);
    expect(sceneLines[2]).toContain('scene bg_black');
  });

  it('shows particles when effects mention sakura', () => {
    expect(script).toContain('show sakura');
  });

  it('applies heart_pulse layer transform when storyboard mentions it', () => {
    expect(script).toContain('show layer master at heart_pulse');
  });

  it('emits narrator dialogue using the v character', () => {
    expect(script).toMatch(/^ {4}v "她的声音像晚风。"/m);
  });

  it('escapes double quotes inside dialogue', () => {
    const sb: StoryboarderOutput = {
      shots: [
        {
          shotNumber: 1,
          description: 'x',
          characters: ['白樱'],
          sceneName: 'sakura_night',
          staging: 'solo_center',
          transform: 'stand',
          transition: 'none',
          effects: [],
          dialogueLines: [{ speaker: '白樱', text: 'She said "hi"' }],
        },
      ],
    };
    const out = renderScriptRpy(PLANNER_FIXTURE, sb);
    expect(out).toContain('"She said \\"hi\\""');
  });
});

