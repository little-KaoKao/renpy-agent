describe('renderScriptRpy with AssetRegistry (Stage B binding)', () => {
  it('uses real asset paths when registry has ready entries', () => {
    const registry: AssetRegistryFile = {
      version: 1,
      entries: [
        {
          placeholderId: 'pid1',
          logicalKey: logicalKeyForScene('sakura_night'),
          assetType: 'scene_background',
          realAssetLocalPath: 'images/bg/sakura_night.png',
          remoteAssetUri: 'https://cdn/s.png',
          status: 'ready',
          updatedAt: '2026-04-22T00:00:00.000Z',
        },
        {
          placeholderId: 'pid2',
          logicalKey: logicalKeyForCharacter('白樱'),
          assetType: 'character_main',
          realAssetLocalPath: 'images/char/baiying.png',
          remoteAssetUri: 'https://cdn/c.png',
          status: 'ready',
          updatedAt: '2026-04-22T00:00:00.000Z',
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE, registry);
    expect(script).toContain('image bg_sakura_night = "images/bg/sakura_night.png"');
    expect(script).toMatch(/image sprite_\w+ = "images\/char\/baiying\.png"/);
    // Scene without a ready asset still falls back to Solid
    expect(script).toContain('image bg_classroom = Solid(');
  });

  it('renders cutscene shots as placeholder caption when no video is ready', () => {
    const sb: StoryboarderOutput = {
      shots: [
        {
          shotNumber: 1,
          description: 'opening title sweep',
          characters: [],
          sceneName: 'sakura_night',
          staging: 'solo_center',
          transform: 'reset',
          transition: 'fade',
          effects: [],
          cutscene: { kind: 'transition', motionPrompt: 'slow dolly through cherry blossoms' },
          dialogueLines: [{ speaker: 'narrator', text: '春の夜、ひらり。' }],
        },
        {
          shotNumber: 2,
          description: 'first words after the cut',
          characters: ['白樱'],
          sceneName: 'sakura_night',
          staging: 'solo_center',
          transform: 'stand',
          transition: 'dissolve',
          effects: [],
          dialogueLines: [{ speaker: '白樱', text: '你来了。' }],
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, sb);
    expect(script).toContain('cutscene: transition');
    expect(script).toContain('scene bg_black with fade');
    expect(script).toContain('▶ Cutscene placeholder');
    // no movie_cutscene line when registry missing
    expect(script).not.toContain('renpy.movie_cutscene');
    // after a cutscene, the next non-cutscene shot re-issues its scene
    const sceneLines = script.split('\n').filter((l) => l.trim().startsWith('scene bg_'));
    expect(sceneLines.some((l) => l.includes('bg_sakura_night'))).toBe(true);
  });

  it('renders movie_cutscene when registry has a ready cutscene asset', () => {
    const sb: StoryboarderOutput = {
      shots: [
        {
          shotNumber: 1,
          description: 'confession CG',
          characters: ['白樱'],
          sceneName: 'sakura_night',
          staging: 'solo_center',
          transform: 'front',
          transition: 'fade',
          effects: [],
          cutscene: { kind: 'reference', motionPrompt: 'tearful embrace' },
          dialogueLines: [{ speaker: 'narrator', text: 'それは誓い。' }],
        },
      ],
    };
    const registry: AssetRegistryFile = {
      version: 1,
      entries: [
        {
          placeholderId: 'cutscene:cutscene:shot_1',
          logicalKey: logicalKeyForCutscene(1),
          assetType: 'cutscene',
          realAssetLocalPath: 'videos/cut/shot_1.mp4',
          remoteAssetUri: 'https://cdn/confession.mp4',
          status: 'ready',
          updatedAt: '2026-04-23T00:00:00.000Z',
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, sb, registry);
    expect(script).toContain('$ renpy.movie_cutscene("videos/cut/shot_1.mp4")');
    expect(script).not.toContain('▶ Cutscene placeholder');
  });

  it('ignores entries whose status != ready', () => {
    const registry: AssetRegistryFile = {
      version: 1,
      entries: [
        {
          placeholderId: 'p',
          logicalKey: logicalKeyForCharacter('白樱'),
          assetType: 'character_main',
          realAssetLocalPath: 'images/char/wip.png',
          remoteAssetUri: 'https://cdn/c.png',
          status: 'generating',
          updatedAt: '2026-04-22T00:00:00.000Z',
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE, registry);
    expect(script).not.toContain('images/char/wip.png');
  });
});

describe('renderScriptRpy audio + UI insertion (v0.5)', () => {
  it('inserts `play music` after scene header when bgm is ready', () => {
    const registry: AssetRegistryFile = {
      version: 1,
      entries: [
        {
          placeholderId: 'bgm:sakura_night',
          logicalKey: logicalKeyForBgm('sakura_night'),
          assetType: 'bgm_track',
          realAssetLocalPath: 'audio/bgm/sakura_night.mp3',
          remoteAssetUri: 'https://cdn/bgm.mp3',
          status: 'ready',
          updatedAt: '2026-04-25T00:00:00.000Z',
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE, registry);
    expect(script).toContain('play music "audio/bgm/sakura_night.mp3" fadeout 1.0');
    // When classroom bgm missing, no play music line for that scene.
    const playMusicCount = (script.match(/play music /g) ?? []).length;
    expect(playMusicCount).toBe(1);
  });

  it('does not insert `play music` when no bgm registered', () => {
    const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE);
    expect(script).not.toContain('play music');
  });

  it('inserts `voice` before a dialogue line when voice_line is ready', () => {
    // First shot is shotNumber=1, first dialogue line is index 0.
    const registry: AssetRegistryFile = {
      version: 1,
      entries: [
        {
          placeholderId: 'voice:shot_1:line_0',
          logicalKey: logicalKeyForVoiceLine(1, 0),
          assetType: 'voice_line',
          realAssetLocalPath: 'audio/voice/shot_1/line_0.mp3',
          remoteAssetUri: 'https://cdn/v.mp3',
          status: 'ready',
          updatedAt: '2026-04-25T00:00:00.000Z',
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE, registry);
    expect(script).toContain('voice "audio/voice/shot_1/line_0.mp3"');
    // voice line sits directly before its say — find its index and confirm the
    // next non-blank line is a say statement.
    const lines = script.split('\n');
    const voiceIdx = lines.findIndex((l) => l.includes('voice "audio/voice/shot_1/line_0.mp3"'));
    expect(voiceIdx).toBeGreaterThan(-1);
    expect(lines[voiceIdx + 1]).toMatch(/"你又来了。"/);
  });

  it('inserts `play sound` at shot entry when sfx enter cue is ready', () => {
    const registry: AssetRegistryFile = {
      version: 1,
      entries: [
        {
          placeholderId: 'sfx:shot_1:enter',
          logicalKey: logicalKeyForSfx(1, 'enter'),
          assetType: 'sfx',
          realAssetLocalPath: 'audio/sfx/shot_1_enter.mp3',
          remoteAssetUri: 'https://cdn/s.mp3',
          status: 'ready',
          updatedAt: '2026-04-25T00:00:00.000Z',
        },
      ],
    };
    const script = renderScriptRpy(PLANNER_FIXTURE, STORYBOARDER_FIXTURE, registry);
    expect(script).toContain('play sound "audio/sfx/shot_1_enter.mp3"');
  });
});

